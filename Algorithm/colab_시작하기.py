def find_col(df_cols, candidates):
    cols = list(df_cols)
    norm = {str(c).strip().lower(): c for c in cols}
    for cand in candidates:
        k = str(cand).strip().lower()
        if k in norm:
            return norm[k]
    for cand in candidates:
        ck = str(cand).strip().lower().replace("_","")
