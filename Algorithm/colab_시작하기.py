def make_weights_from_rank(idx, power=1.0):
    """
    Weight higher-ranked items more.
    power>1 emphasizes top-most items more.
    """
    L = len(idx)
    # rank 0 highest weight, rank L-1 lowest
    r = np.linspace(1.0, 0.0, L)
    w = (r ** power)
    # avoid all-zero
    if np.sum(w) <= 1e-12:
        w = np.ones(L)
    return w / np.sum(w)

